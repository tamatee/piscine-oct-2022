int	ft_str_is_numeric(char *str)
{
	int		i;
	int		a;
	char	cur;

	i = 0;
	a = 1;
	while (str[i])
	{
		cur = str[i];
		if (str[i] == '\0')
		{
			break ;
		}
		if (!(cur >= '0' && cur <= '9'))
		{
			a = 0;
			break ;
		}
		i++;
	}
	return (a);
}
