void	setzero(int m, int n, int arr[m][n])
{
	int	i;
	int	j;

	i = 0;
	while (i < m)
	{
		j = 0;
		while (j < n)
		{
			arr[i][j] = 0;
			j++;
		}
		i++;
	}
}
