void	pt_7(int n, int point[n][n], int clue[n][n], int band[n][n])
{
	int	x;

	x = 0;
	while (x < n)
	{
		if (clue[0][x] == 1 && clue[1][x] == 3
			&& point[0][x] == 4 && point[3][x] == 1)
		{		
				point[1][x] = 2;
				point[2][x] = 3;
		}
	}
}
